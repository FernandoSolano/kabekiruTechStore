package cr.ac.ucr.kabekuritechstore.domain;

public class Role {
	
	private int rolID;
	private String type;
	
	
	public Role() {
		
	}


	public int getRolID() {
		return rolID;
	}


	public void setRolID(int rolID) {
		this.rolID = rolID;
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}
	
	

}
